 CREATE TABLE "SYSTEM"."CUSTOMER" 
   (	"CUSTOMER_ID" NUMBER, 
	"NAME" VARCHAR2(50 BYTE) CONSTRAINT "NN_CUSTOMER_NAME" NOT NULL ENABLE, 
	"PHONE_NUM" VARCHAR2(15 BYTE) CONSTRAINT "NN_CUSTOMER_PHONE" NOT NULL ENABLE, 
	"ADDRESS" VARCHAR2(100 BYTE) CONSTRAINT "NN_CUSTOMER_ADDRESS" NOT NULL ENABLE, 
	"AREA" VARCHAR2(50 BYTE) CONSTRAINT "NN_CUSTOMER_AREA" NOT NULL ENABLE, 
	"ACCOUNT_STATUS" VARCHAR2(20 BYTE) DEFAULT 'Active', 
	 CONSTRAINT "CHK_CUSTOMER_STATUS" CHECK (Account_Status IN ('Active','Inactive','Blocked')) ENABLE, 
	 CONSTRAINT "PK_CUSTOMER" PRIMARY KEY ("CUSTOMER_ID")
    )